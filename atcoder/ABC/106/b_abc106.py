# 105 = 3 * 5 * 7
# 1, 3, 5, 7, 15, 21, 35, 105
# 3 つの 素数を1回ずつ掛けてできる数(素因数が3つ)
# 3, 5, 7, 11, 13, 17
# 5 * 3 * 3 * 3 = 135
# 3 * 5 * 11 = 165
# 7 * 3 * 3 * 3 = 189
# 3 * 5 * 13 = 195

# 105以下では1つ(105のみ)なら, 200以下の整数なら, そういう奇数は105のみ?

prime = [3, 5, 11, 13, 17]

N = int(input())

if N >= 195:
    print(5)
elif N >= 189:
    print(4)
elif N >= 165:
    print(3)
elif N >= 135:
    print(2)
elif N >= 105:
    print(1)
else:
    print(0)

